<h2><span style="text-transform: none">jAutoInvoice</span></h2>

<p>
    O sistema <strong>jAutoInvoice</strong> permite a gestão de oficinas de automóveis fornecendo acesso a um 
    conjunto de funcionalidades desenhadas para melhorar a gestão sem aumentar a complexidade ou exigir demasiados 
    conhecimentos técnicos.
</p>

<h3>Software Livre</h3>
<p>
    Desenvolvido em cima de <em>software livre</em> de qualidade, o <strong>jAutoInvoice</strong> é também ele um software
    disponível através de uma licença livre permitindo dessa forma a adaptação ou adequação do sistema às 
    necessidades específicas de uma empresa.    
</p>
<p>
    O <em>software livre</em> caracteriza-se por permitir, legalmente, o estudo, modificação, alteração e 
    partilha na forma original ou modificada. Estas liberdades impedem um utilizador de ficar preso ao software ou 
    de usar apenas o que o fabricante do software determina.
</p>

